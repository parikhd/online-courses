a, b = 8, 4
print("Add: ", a + b)
print("Sub: ", a - b)
print("Div: ", a / b)
print("Mul: ", a * b)
print("Pow: ", a ** b)
print("Mod: ", a % b)
print("Floor Div: ", a // b)  # Take only integer part of result
print(1 // 2)  # 0.5 so 0

# Assignment Operators

a += 1
print(a)  # 9

# Comparison Operators

print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b)
print(a != b)

# Logical Operators
print("Logical")
print(a == 9 and b == 10)
print(a == 9 and not(b == 10))
print(a == 9 or b == 10)
